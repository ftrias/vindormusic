class FingerModel:
    def __init__(self, model=[], name="", desc=None, bits=12, mask=None, oct=1, expand=False):
        self.model = model
        self.name = name
        if desc is None:
          self.desc = name
        else:
          self.desc = desc
        self.bits = bits
        if mask is None:
            self.mask = (1<<bits)-1
        else:
            if isinstance(mask, str):
                self.mask = eval(mask)
            else:
                self.mask = mask
        self.oct = oct
        self.expand = expand
    
